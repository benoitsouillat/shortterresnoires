            <section>
                <h5 id="commune" class="indent">Vivre mon animal et ma commune</h5>
                <article>
                    <span class="article-number">Article L211-14-1</span><br />
                    Une évaluation comportementale peut être demandée par le maire pour tout chien qu'il désigne
                    en application de l'article L. 211-11.
                    Cette évaluation est effectuée par un vétérinaire choisi sur une liste départementale.
                    Elle est communiquée au maire par le vétérinaire.<br />
                    Les frais d'évaluation sont à la charge du propriétaire du chien.<br />
                    Un décret détermine les conditions d'application du présent article.
                </article>
                <article>
                    <span class="article-number">Article L211-14-2</span><br />
                    Tout fait de morsure d'une personne par un chien est déclaré par son propriétaire ou son détenteur
                    ou par tout professionnel en ayant connaissance dans l'exercice de ses fonctions à la mairie de la
                    commune de résidence
                    du propriétaire ou du détenteur de l'animal.<br />

                    Le propriétaire ou le détenteur du chien est en outre tenu de le soumettre,
                    pendant la période de surveillance définie en application du premier alinéa de l'article L. 223-10,
                    à l'évaluation comportementale mentionnée à l'article L. 211-14-1, qui est communiquée au
                    maire.<br />

                    A la suite de cette évaluation, le maire ou, à défaut, le préfet peut imposer au propriétaire
                    ou au détenteur du chien de suivre la formation et d'obtenir l'attestation d'aptitude mentionnées à
                    l'article L. 211-13-1.<br />

                    Faute pour l'intéressé de s'être soumis à ces obligations, le maire ou, à défaut,
                    le préfet peut ordonner par arrêté que l'animal soit placé dans un lieu de dépôt adapté à la garde
                    de celui-ci.
                    Il peut, en cas de danger grave et immédiat et après avis d'un vétérinaire désigné par le préfet,
                    faire procéder à son euthanasie.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-21</span><br />
                    Les maires prescrivent que les animaux d'espèce sauvage apprivoisés ou tenus en captivité, trouvés
                    errants et qui sont saisis
                    sur le territoire de la commune, sont conduits à un lieu de dépôt désigné par eux.
                    Ces animaux y sont maintenus aux frais du propriétaire ou du détenteur.<br />
                    Les propriétaires, locataires, fermiers ou métayers peuvent saisir ou faire saisir par un agent de
                    la force publique,
                    dans les propriétés dont ils ont l'usage, les animaux d'espèce sauvage apprivoisés ou tenus en
                    captivité,
                    échappés à leur détenteur ou que celui-ci laisse divaguer. Les animaux saisis sont conduits à un
                    lieu de dépôt désigné par le maire.
                    Ils y sont maintenus, le cas échéant, aux frais du propriétaire ou du détenteur.<br />
                    A l'issue d'un délai franc de garde de huit jours ouvrés au lieu de dépôt désigné,
                    si l'animal n'a pas été réclamé par son propriétaire auprès du maire de la commune où l'animal a été
                    saisi,
                    il est alors considéré comme abandonné et le maire peut le céder ou, après avis d'un vétérinaire, le
                    faire euthanasier.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-22</span><br />
                    Les maires prennent toutes dispositions propres à empêcher la divagation des chiens et des
                    chats.<br />
                    Ils peuvent ordonner que ces animaux soient tenus en laisse et que les chiens soient muselés. <br />
                    Ils prescrivent que les chiens et les chats errants et tous ceux qui seraient saisis sur le
                    territoire de la commune sont conduits à la fourrière,
                    où ils sont gardés pendant les délais fixés aux articles L. 211-25 et L. 211-26.<br />
                    Les propriétaires, locataires, fermiers ou métayers peuvent saisir ou faire saisir par un agent de
                    la force publique,
                    dans les propriétés dont ils ont l'usage, les chiens et les chats que leurs maîtres laissent
                    divaguer. <br />
                    Les animaux saisis sont conduits à la fourrière.
                    Un décret en Conseil d'Etat détermine les modalités d'application du présent article.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-24</span><br />
                    Chaque commune doit disposer soit d'une fourrière communale apte à l'accueil
                    et à la garde des chiens et chats trouvés errants ou en état de divagation
                    jusqu'au terme des délais fixés aux articles L. 211-25 et L. 211-26, soit du service
                    d'une fourrière établie sur le territoire d'une autre commune,
                    avec l'accord de cette commune.<br />
                    Chaque fourrière doit avoir une capacité adaptée aux besoins de chacune des communes
                    pour lesquelles elle assure le service d'accueil des animaux en
                    application du présent code. La capacité de chaque fourrière est constatée par arrêté
                    du maire de la commune où elle est installée.<br />
                    La surveillance dans la fourrière des maladies classées parmi les dangers sanitaires de première
                    et deuxième catégories au titre de l'article L. 221-1
                    est assurée par un vétérinaire sanitaire désigné par le gestionnaire de la fourrière,
                    dans les conditions prévues par la section 1 du chapitre III
                    du titre préliminaire.<br />
                    Les animaux ne peuvent être restitués à leur propriétaire qu'après paiement des frais de fourrière.
                    En cas de non-paiement, le propriétaire
                    est passible d'une amende forfaitaire dont les modalités sont définies par décret.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-25</span><br />
                    I.-Lorsque les chiens et les chats accueillis dans la fourrière sont identifiés conformément à
                    l'article L. 212-10
                    ou par le port d'un collier où figurent le nom et l'adresse de leur maître, le gestionnaire de la
                    fourrière recherche,
                    dans les plus brefs délais, le propriétaire de l'animal. Dans les départements officiellement
                    déclarés infectés par la rage,
                    seuls les animaux vaccinés contre la rage peuvent être rendus à leur propriétaire.<br />
                    A l'issue d'un délai franc de garde de huit jours ouvrés, si l'animal n'a pas été réclamé par son
                    propriétaire,
                    il est considéré comme abandonné et devient la propriété du gestionnaire de la fourrière, qui peut
                    en disposer
                    dans les conditions définies ci-après.<br />
                    II.-Dans les départements indemnes de rage, le gestionnaire de la fourrière peut garder les animaux
                    dans la limite
                    de la capacité d'accueil de la fourrière. Après avis d'un vétérinaire, <br />
                    le gestionnaire peut céder les animaux à titre gratuit à des fondations ou des associations de
                    protection des animaux disposant d'un refuge qui,
                    seules, sont habilitées à proposer les animaux à l'adoption à un nouveau propriétaire. <br />
                    Ce don ne peut intervenir que si le bénéficiaire s'engage à respecter les exigences liées à la
                    surveillance vétérinaire de l'animal,
                    dont les modalités et la durée sont fixées par arrêté du ministre chargé de l'agriculture.
                    Après l'expiration du délai de garde, si le vétérinaire en constate la nécessité, il procède à
                    l'euthanasie de l'animal.<br />
                    III.-Dans les départements officiellement déclarés infectés de rage,
                    il est procédé à l'euthanasie des animaux non remis à leur propriétaire à l'issue du délai de garde.
                </article>
            </section>
            <section>
                <h5 id="possession" class="indent">Posséder un animal - Nos droits et devoirs</h5>
                <article>
                    <span class="article-number">Article L211-19-1</span><br />
                    Il est interdit de laisser divaguer les animaux domestiques
                    et les animaux sauvages apprivoisés ou tenus en captivité.
                </article>
                <article>
                    <span class="article-number">Article L211-20</span><br />
                    Lorsque des animaux errants sans détenteur, ou dont le détenteur refuse de se faire connaître,
                    sont trouvés pacageant sur des terrains appartenant à autrui, sur les accotements ou dépendances des
                    routes,
                    canaux, chemins ou sur des terrains communaux, le propriétaire lésé, ou son représentant,
                    a le droit de les conduire ou de les faire
                    conduire immédiatement au lieu de dépôt désigné par l'autorité municipale.<br />
                    Le maire donne avis au propriétaire ou au détenteur des animaux des dispositions mises en oeuvre.
                    Si les animaux ne sont pas réclamés, ils sont considérés comme abandonnés
                    et le maire fait procéder soit à leur euthanasie, soit à leur vente conformément aux dispositions de
                    l'article L. 211-1,
                    soit à leur cession, à titre gratuit, à une fondation ou à une association de protection animale
                    reconnue d'utilité publique ou déclarée. <br />
                    Les frais résultant de l'ensemble des mesures prises sont mis à la charge du propriétaire ou du
                    détenteur des animaux.
                    Si le propriétaire ou le détenteur des animaux demeure inconnu,
                    le maire autorise le gestionnaire du lieu de dépôt à prendre l'une des mesures énumérées ci-dessus.
                </article>
                <article>
                    <span class="article-number">Article L211-23</span><br />
                    Est considéré comme en état de divagation tout chien qui, en dehors d'une action de chasse ou de la
                    garde ou de la protection du troupeau,
                    n'est plus sous la surveillance effective de son maître, se trouve hors de portée de voix de
                    celui-ci ou de tout instrument sonore permettant son rappel,
                    ou qui est éloigné de son propriétaire ou de la personne qui en est responsable d'une distance
                    dépassant cent mètres. <br />
                    Tout chien abandonné, livré à son seul instinct, est en état de divagation, sauf s'il participait à
                    une action de chasse et
                    qu'il est démontré que son propriétaire ne s'est pas abstenu de tout entreprendre pour le retrouver
                    et le récupérer,
                    y compris après la fin de l'action de chasse.<br />
                    Est considéré comme en état de divagation tout chat non identifié trouvé à plus de deux cents mètres
                    des habitations
                    ou tout chat trouvé à plus de mille mètres du domicile de son maître et qui n'est pas sous la
                    surveillance immédiate de celui-ci,
                    ainsi que tout chat dont le propriétaire n'est pas connu et qui est saisi sur la voie publique ou
                    sur la propriété d'autrui.<br />
                </article>
                <article>
                    <span class="article-number">Article R211-3 </span><br />
                    Tout chien circulant sur la voie publique, en liberté ou même tenu en laisse,
                    doit être muni d'un collier portant, gravés sur une plaque de métal, les nom et adresse de son
                    propriétaire. <br />
                    Sont exceptés de cette prescription les chiens courants portant la marque de leur maître.
                </article>
            </section>
            <section>
                <h5 id="identification" class="indent">L'identification chez le chien</h5>
                <article>
                    <span class="article-number">Article L211-26</span><br />
                    I.-Dans les départements indemnes de rage, lorsque les chiens et les chats accueillis dans la
                    fourrière ne sont pas identifiés,
                    les animaux sont gardés pendant un délai franc de huit jours ouvrés. L'animal ne peut être remis à
                    son propriétaire
                    qu'après avoir été identifié
                    conformément à l'article L. 212-10. Les frais de l'identification sont à la charge du
                    propriétaire.<br />
                    Si, à l'issue de ce délai, l'animal n'a pas été réclamé par son propriétaire,
                    il est considéré comme abandonné et devient la propriété du gestionnaire de la fourrière, qui peut
                    en disposer
                    dans les mêmes conditions que celles mentionnées au II de l'article L. 211-25.
                    <br />
                    II.-Dans les départements officiellement déclarés infectés de rage,
                    il est procédé à l'euthanasie des chiens et des chats non identifiés admis à la fourrière.
                </article>
                <article>
                    <span class="article-number">Article D211-3-1-1 </span><br />
                    Tout vétérinaire en exercice inscrit au tableau de l'ordre peut être inscrit sur une ou plusieurs
                    listes départementales.
                    La demande d'inscription est adressée, par écrit, au conseil régional de l'ordre des vétérinaires
                    dans le ressort duquel
                    le vétérinaire a déclaré son domicile professionnel administratif. Un arrêté du ministre chargé de
                    l'agriculture fixe
                    la composition du dossier de demande d'inscription. <br />
                    La liste départementale mentionne l'identité, l'adresse du ou des domiciles professionnels
                    d'exercice et les coordonnées téléphoniques du vétérinaire praticien.
                </article>
                <article>
                    <span class="article-number">Article L212-10</span><br />
                    Les chiens et chats, préalablement à leur cession, à titre gratuit ou onéreux,
                    sont identifiés par un procédé agréé par le ministre chargé de l'agriculture mis en œuvre par les
                    personnes
                    qu'il habilite à cet effet. <br />
                    Il en est de même, en dehors de toute cession, pour les chiens nés après le 6 janvier 1999 âgés de
                    plus de quatre mois
                    et pour les chats de plus de sept mois nés après le 1er janvier 2012. L'identification est à la
                    charge du cédant.<br />

                    Dans les départements officiellement déclarés infectés de rage, l'identification est obligatoire
                    pour tous les carnivores domestiques.
                </article>
                <article>
                    <span class="article-number">Article L212-12</span><br />
                    Des décrets, précisés, le cas échéant, par des arrêtés du ministre chargé de l'agriculture,
                    rendent obligatoires et définissent les méthodes et conditions suivant lesquelles est assurée
                    l'identification des animaux.
                </article>
            </section>
            <section>
                <h5 id="bienetre" class="indent">Le bien-être chez les animaux et leur protection</h5>
                <article>
                    <span class="article-number">Article L214-1 </span><br />
                    Tout animal étant un être sensible doit être placé par son propriétaire
                    dans des conditions compatibles avec les impératifs biologiques de son espèce.
                </article>
                <article>
                    <span class="article-number">Article L214-2 </span><br />
                    Tout homme a le droit de détenir des animaux dans les conditions définies à l'article L. 214-1 et de
                    les utiliser
                    dans les conditions prévues à l'article L. 214-3, sous réserve des droits des tiers et des exigences
                    de la sécurité et de
                    l'hygiène publique et des dispositions de la loi n° 76-629 du 10 juillet 1976 relative à la
                    protection de la nature. <br />

                    Les établissements ouverts au public pour l'utilisation d'animaux sont soumis au contrôle de
                    l'autorité administrative
                    qui peut prescrire des mesures pouvant aller jusqu'à la fermeture de l'établissement, indépendamment
                    des poursuites pénales q
                    ui peuvent être exercées au titre de la loi précitée. Un décret en Conseil d'Etat précise les
                    modalités d'application du présent article
                    et de l'article L. 214-1.
                </article>
                <article>
                    <span class="article-number">Article L214-3 </span><br />
                    Il est interdit d'exercer des mauvais traitements envers les animaux domestiques ainsi qu'envers les
                    animaux sauvages apprivoisés ou tenus en captivité. <br />

                    Des décrets en Conseil d'Etat déterminent les mesures propres à assurer la protection de ces animaux
                    contre les mauvais traitements ou les utilisations abusives et à leur éviter des souffrances lors
                    des manipulations inhérentes aux diverses techniques d'élevage, de parcage, de transport et
                    d'abattage des animaux.<br />

                    Il en est de même pour ce qui concerne les expériences biologiques médicales et scientifiques qui
                    doivent être limitées aux cas de stricte nécessité.<br />
                </article>
                <article>
                    <span class="article-number">Article L214-4 </span><br />
                    L'attribution en lot ou prime de tout animal vivant, à l'exception des animaux d'élevage dans le
                    cadre de fêtes, foires, manifestations sportives, folkloriques et locales traditionnelles, concours
                    et manifestations à caractère agricole, est interdite. Le représentant de l'Etat dans le département
                    concerné établit la liste des manifestations sportives, folkloriques et locales traditionnelles pour
                    lesquelles cette interdiction ne s'applique pas.
                </article>
                <article>
                    <span class="article-number">Article L214-5 </span><br />
                    Le ministre chargé de l'agriculture peut désigner des centres nationaux de référence en matière de
                    bien-être animal, chargés notamment d'apporter une expertise technique et de contribuer à la
                    diffusion des résultats de la recherche et des innovations techniques.
                </article>



            </section>
            <section>
                <h5 id="metier" class="indent">Les métiers autour du chien</h5>
                <article>
                    <span class="article-number">Article L214-6-1 </span><br />
                    I.-La gestion d'une fourrière ou d'un refuge, ainsi que l'exercice à titre commercial des activités
                    de transit ou de garde, d'éducation, de dressage et de présentation au public de chiens et de chats
                    :<br />

                    <span class="indent"></span>1° Font l'objet d'une déclaration au préfet ; <br />

                    <span class="indent"></span>2° Sont subordonnés à la mise en place et à l'utilisation
                    d'installations conformes aux règles sanitaires et de protection animale pour ces animaux ;<br />

                    <span class="indent"></span>3° Ne peuvent s'exercer que si au moins une personne, en contact direct
                    avec les animaux, peut justifier soit :<br />


                    <span class="indent-lg"></span>-être en possession d'une certification professionnelle dont la liste
                    est établie par le ministre chargé de l'agriculture ;<br />

                    <span class="indent-lg"></span>-avoir suivi une formation dans un établissement habilité par le
                    ministre chargé de l'agriculture afin d'acquérir les connaissances relatives aux besoins
                    biologiques, physiologiques, comportementaux et à l'entretien des animaux de compagnie et disposer
                    d'une attestation de connaissance établie par l'autorité administrative ;<br />

                    <span class="indent-lg"></span>-posséder un certificat de capacité délivré par l'autorité
                    administrative en application des dispositions du IV de l'article L. 214-6 dans sa rédaction en
                    vigueur antérieurement à la publication de l'ordonnance n° 2015-1243 du 7 octobre 2015 relative au
                    commerce et à la protection des animaux de compagnie.<br />

                    Les prestations de services effectuées en France, à titre temporaire et occasionnel, par les
                    professionnels ressortissants d'un Etat membre de l'Union européenne ou d'un autre Etat partie à
                    l'accord sur l'Espace économique européen établis sur le territoire d'un de ces Etats sont régies
                    par l'article L. 204-1 et, le cas échéant, par l'article L. 204-2.<br />

                    Les mêmes dispositions s'appliquent pour l'exercice à titre commercial des activités de présentation
                    au public des autres animaux de compagnie d'espèces domestiques.<br />

                    II.-Les personnes qui, sans exercer les activités mentionnées au I ou aux articles L. 214-6-2 et L.
                    214-6-3, détiennent plus de neuf chiens sevrés doivent mettre en place et utiliser des installations
                    conformes aux règles sanitaires et de protection animale pour ces animaux.<br />

                    III.-Seules les associations de protection des animaux reconnues d'utilité publique ou les
                    fondations ayant pour objet la protection des animaux peuvent gérer des établissements dans lesquels
                    les actes vétérinaires sont dispensés gratuitement aux animaux des personnes dépourvues de
                    ressources suffisantes.

                    La gestion de ces établissements est subordonnée à une déclaration auprès du préfet du département
                    où ils sont installés.

                    Les conditions sanitaires et les modalités de contrôle correspondantes sont fixées par décret.<br />

                    IV.-L'activité de toilettage des chiens et des chats doit être exercée dans des installations
                    conformes aux règles sanitaires et de protection animale applicables à ces animaux.
                </article>
                <article>
                    <span class="article-number">Article L214-6-2 </span><br />
                    I.-Toute personne exerçant l'activité d'élevage de chiens ou de chats au sens du III de l'article L.
                    214-6 est tenue de s'immatriculer dans les conditions prévues à l'article L. 311-2-1 et de se
                    conformer aux conditions énumérées au I de l'article L. 214-6-1.<br />

                    II.-Toutefois, les éleveurs de chats et chiens ne cédant à titre onéreux pas plus d'une portée de
                    chiens ou de chats par an et par foyer fiscal sont dispensés des formalités prévues aux 1° et 3° du
                    I de l'article L. 214-6-1.<br />

                    III.-Les éleveurs produisant uniquement des chiens et chats inscrits au livre généalogique reconnu
                    par le ministre chargé de l'agriculture sont dispensés des mêmes formalités, ainsi que de
                    l'immatriculation prévue au premier alinéa du présent article lorsqu'ils cèdent les chiens et les
                    chats à titre onéreux, sous réserve qu'ils respectent les conditions suivantes et en justifient sur
                    demande aux agents habilités à rechercher et constater les manquements aux dispositions de la
                    présente section :<br />

                    <span class="indent"></span>1° Ne pas vendre plus d'une portée de chiens ou de chats par an et par
                    foyer fiscal ;<br />

                    <span class="indent"></span>2° Déclarer au livre généalogique reconnu par le ministre chargé de
                    l'agriculture, pour l'obtention d'un numéro spécifique à la portée, l'ensemble des portées issues
                    des chiens ou chats qu'ils détiennent et qui sont inscrits au livre généalogique selon des modalités
                    définies par décret.
                </article>
                <article>
                    <span class="article-number">Article L214-6-3 </span><br />
                    L'exercice à titre commercial d'activités de vente d'animaux de compagnie au sens du IV de l'article
                    L. 214-6 est subordonné à l'immatriculation prévue à l'article L. 123-1 du code de commerce, ainsi
                    qu'au respect des conditions énumérées au I de l'article L. 214-6-1.
                </article>
                <article>
                    <span class="article-number">Article L214-7</span><br />
                    La cession, à titre gratuit ou onéreux, des chiens et des chats et autres animaux de compagnie est
                    interdite dans les foires, marchés, brocantes, salons, expositions ou toutes autres manifestations
                    non spécifiquement consacrés aux animaux.<br />

                    Le préfet peut autoriser des opérations de ventes d'animaux de compagnie autres que les chiens et
                    les chats pendant une ou plusieurs périodes prédéfinies, par des professionnels exerçant des
                    activités de vente dans des foires et marchés non spécifiquement consacrés aux animaux. Cette
                    autorisation est subordonnée à la mise en place et l'utilisation d'installations conformes aux
                    règles sanitaires et de protection animale en vigueur.<br />

                    L'organisateur d'une exposition ou de toute autre manifestation consacrée à des animaux de compagnie
                    est tenu d'en faire préalablement la déclaration au préfet du département et de veiller à la mise en
                    place et à l'utilisation, lors de cette manifestation, d'installations conformes aux règles
                    sanitaires et de protection animale.<br />
                </article>
                <article>
                    <span class="article-number">Article L214-8 </span><br />
                    La vente en libre-service d'un animal vertébré est interdite.<br /><br />

                    I.-Toute vente d'animaux de compagnie réalisée dans le cadre des activités prévues aux articles L.
                    214-6-1 à L. 214-6-3 doit s'accompagner, au moment de la livraison à l'acquéreur, de la délivrance
                    :<br />

                    <span class="indent"></span>1° D'une attestation de cession ;<br />

                    <span class="indent"></span>2° D'un document d'information sur les caractéristiques et les besoins
                    de l'animal contenant également, au besoin, des conseils d'éducation ;<br />

                    <span class="indent"></span>3° Pour les ventes de chiens ou de chats, d'un certificat vétérinaire
                    dans des conditions définies par décret.<br />

                    La facture tient lieu d'attestation de cession pour les transactions réalisées entre des
                    professionnels.<br />

                    Les dispositions du présent article sont également applicables à toute cession, à titre gratuit ou
                    onéreux, par une association de protection des animaux ou une fondation consacrée à la protection
                    des animaux.<br />

                    II.-Seuls les chiens et les chats âgés de plus de huit semaines peuvent faire l'objet d'une cession
                    à titre gratuit ou onéreux.<br />

                    III.-Ne peuvent être dénommés comme chiens ou chats appartenant à une race que les chiens ou les
                    chats inscrits à un livre généalogique reconnu par le ministre chargé de l'agriculture.<br />

                    IV.-Toute cession d'un chat ou d'un chien, dans des conditions autres que celles mentionnées au I,
                    est subordonnée à la délivrance du certificat vétérinaire mentionné au 3° du I.<br />
                </article>
                <article>
                    <span class="article-number">Article L214-8-1 </span><br />
                    Toute publication d'une offre de cession de chats ou de chiens fait figurer :<br />

                    <span class="indent-lg"></span>-l'âge des animaux ;<br />

                    <span class="indent-lg"></span>-l'existence ou l'absence d'inscription de ceux-ci à un livre
                    généalogique reconnu par le ministre chargé de l'agriculture, le cas échéant, le numéro
                    d'identification de chaque animal ou le numéro d'identification de la femelle ayant donné naissance
                    aux animaux, le nombre d'animaux de la portée.<br />

                    Toute publication d'une offre de cession à titre onéreux de chats ou de chiens, quel que soit le
                    support utilisé, doit mentionner le numéro d'immatriculation prévu au I de l'article L. 214-6-2 et à
                    l'article L. 214-6-3 ou, pour les éleveurs qui satisfont aux conditions prévues au III de l'article
                    L. 214-6-2, le numéro de portée attribué dans le livre généalogique reconnu par le ministre chargé
                    de l'agriculture.<br />

                    Toute publication d'une offre de cession à titre gratuit doit mentionner explicitement le caractère
                    de don ou de gratuité.
                </article>
            </section>

