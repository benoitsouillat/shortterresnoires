                <h5 id="dangereux" class="indent">Détention d'animaux dangereux ou errants</h5>
                <article>
                    <span class="article-number">Article L211-11</span><br />
                    I.-Si un animal est susceptible, compte tenu des modalités de sa garde, de présenter un danger pour
                    les personnes ou les animaux domestiques,
                    le maire ou, à défaut, le préfet peut prescrire à son propriétaire ou à son détenteur de prendre des
                    mesures de nature à prévenir le danger.
                    Il peut à ce titre, à la suite de l'évaluation comportementale d'un chien réalisée en application de
                    l'article L. 211-14-1,
                    imposer à son propriétaire ou à son détenteur de suivre la formation et d'obtenir l'attestation
                    d'aptitude prévues au I de
                    l'article L. 211-13-1.<br />
                    En cas d'inexécution, par le propriétaire ou le détenteur de l'animal, des mesures prescrites, le
                    maire peut, par arrêté,
                    placer l'animal dans un lieu de dépôt adapté à l'accueil et à la garde de celui-ci.<br />
                    Si, à l'issue d'un délai franc de garde de huit jours ouvrés, le propriétaire ou le détenteur ne
                    présente pas toutes les garanties
                    quant à l'application des mesures prescrites, le maire autorise le gestionnaire du lieu de dépôt,
                    après avis d'un vétérinaire désigné
                    par le préfet, soit à faire procéder à l'euthanasie de l'animal, soit à en disposer dans les
                    conditions prévues au II de l'article L. 211-25.<br />
                    Le propriétaire ou le détenteur de l'animal est invité à présenter ses observations avant la mise en
                    oeuvre des dispositions
                    du deuxième alinéa du présent I.<br /><br />
                    II.-En cas de danger grave et immédiat pour les personnes ou les animaux domestiques,
                    le maire ou à défaut le préfet peut ordonner par arrêté que l'animal soit placé dans un lieu de
                    dépôt adapté à la garde de
                    celui-ci et, le cas échéant, faire procéder à son euthanasie.<br />
                    Est réputé présenter un danger grave et immédiat tout chien appartenant à une des catégories
                    mentionnées à l'article L. 211-12,
                    qui est détenu par une personne mentionnée à l'article L. 211-13 ou qui se trouve dans un lieu où sa
                    présence est interdite par le
                    I de l'article L. 211-16, ou qui circule sans être muselé et tenu en laisse dans les conditions
                    prévues par le II du même article,
                    ou dont le propriétaire ou le détenteur n'est pas titulaire de l'attestation d'aptitude prévue au I
                    de l'article L. 211-13-1.<br />

                    L'euthanasie peut intervenir sans délai, aprés avis d'un vétérinaire désigné par le préfet.
                    Cet avis doit être donné au plus tard quarante-huit heures après le placement de l'animal.A défaut,
                    l'avis est réputé favorable à l'euthanasie.<br /><br />

                    III.-Les frais afférents aux opérations de capture, de transport de garde et d'euthanasie de
                    l'animal sont intégralement
                    et directement mis à la charge de son propriétaire ou de son détenteur.<br />

                </article>
                <article>
                    <span class="article-number">Article L211-12</span><br />
                    Les types de chiens susceptibles d'être dangereux faisant l'objet des mesures spécifiques prévues
                    par les articles L. 211-13, L. 211-13-1, L. 211-14, L. 211-15 et L. 211-16, sans préjudice des
                    dispositions de l'article L. 211-11, sont répartis en deux catégories :
                    <br /><br />
                    <span class="indent"></span>1° Première catégorie : les chiens d'attaque ;
                    <br />
                    <span class="indent"></span>2° Deuxième catégorie : les chiens de garde et de défense.
                    <br />
                    Un arrêté du ministre de l'intérieur et du ministre chargé de l'agriculture établit la liste des
                    types de chiens relevant de chacune de ces catégories.
                </article>
                <article>
                    <span class="article-number">Article L211-13 </span><br />
                    Ne peuvent détenir les chiens mentionnés à l'article L. 211-12 : <br />

                    <span class="indent"></span>1° Les personnes âgées de moins de dix-huit ans ;<br />

                    <span class="indent"></span>2° Les majeurs en tutelle à moins qu'ils n'y aient été autorisés par le
                    juge des tutelles ;<br />

                    <span class="indent"></span>3° Les personnes condamnées pour crime ou à une peine d'emprisonnement
                    avec ou sans sursis pour délit inscrit au bulletin n° 2
                    du casier judiciaire ou, pour les ressortissants étrangers, dans un document équivalent ;<br />

                    <span class="indent"></span>4° Les personnes auxquelles la propriété ou la garde d'un chien a été
                    retirée en application de l'article L. 211-11.
                    Le maire peut accorder une dérogation à l'interdiction en considération du comportement du demandeur
                    depuis la décision de retrait,
                    à condition que celle-ci ait été prononcée plus de dix ans avant le dépôt de la déclaration visée à
                    l'article L. 211-14.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-13-1</span><br />
                    I.-Le propriétaire ou le détenteur d'un chien mentionné à l'article L. 211-12 est tenu d'être
                    titulaire
                    d'une attestation d'aptitude sanctionnant une formation portant sur l'éducation et le comportement
                    canins,
                    ainsi que sur la prévention des accidents.<br />

                    Les frais afférents à cette formation sont à la charge du propriétaire ou du détenteur du
                    chien.<br />

                    Un décret en Conseil d'Etat définit le contenu de la formation et les modalités d'obtention de
                    l'attestation d'aptitude.
                    Il détermine également les conditions d'agrément et de contrôle des personnes habilitées à dispenser
                    la formation et à délivrer l'attestation d'aptitude.<br /><br />

                    II.-Le propriétaire ou le détenteur d'un chien mentionné à l'article L. 211-12 est tenu, lorsque le
                    chien est âgé de plus de huit mois et de moins de douze mois,
                    de le soumettre à l'évaluation comportementale prévue à l'article L. 211-14-1.<br />

                    Cette évaluation peut être renouvelée dans des conditions définies par décret.
                    Le maire peut en outre demander à tout moment une nouvelle évaluation en application de l'article L.
                    211-14-1.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-14</span><br />
                    I.-Pour les personnes autres que celles mentionnées à l'article L. 211-13, la détention des chiens
                    mentionnés à l'article L. 211-12 est subordonnée
                    à la délivrance d'un permis de détention par le maire de la commune où le propriétaire ou le
                    détenteur de l'animal réside. En cas de changement de commune de résidence,
                    le permis doit être présenté à la mairie du nouveau domicile.<br />

                    II.-La délivrance du permis de détention est subordonnée à la production :<br />

                    <span class="indent"></span>1° De pièces justifiant :<br />

                    <span class="indent-lg"> </span>a) De l'identification du chien dans les conditions prévues à
                    l'article L. 212-10 ;<br />

                    <span class="indent-lg"> </span>b) De la vaccination antirabique du chien en cours de validité
                    ;<br />

                    <span class="indent-lg"> </span>c) Dans les conditions définies par décret, d'une assurance
                    garantissant la responsabilité civile du propriétaire du chien ou de la personne
                    qui le détient pour les dommages causés aux tiers par l'animal. Les membres de la famille du
                    propriétaire de l'animal ou de celui qui le détient sont considérés
                    comme tiers au sens des présentes dispositions ;<br />

                    <span class="indent-lg"> </span>d) Pour les chiens mâles et femelles de la première catégorie, de la
                    stérilisation de l'animal ;<br />

                    <span class="indent-lg"> </span>e) De l'obtention, par le propriétaire ou le détenteur de l'animal,
                    de l'attestation d'aptitude mentionnée au I de l'article L. 211-13-1 ;<br />

                    <span class="indent"></span>2° De l'évaluation comportementale prévue au II de l'article L.
                    211-13-1.<br />

                    Lorsque le chien n'a pas atteint l'âge auquel cette évaluation doit être réalisée, il est délivré à
                    son propriétaire ou son détenteur un permis provisoire
                    dans des conditions précisées par décret.

                    Si les résultats de l'évaluation le justifient, le maire peut refuser la délivrance du permis de
                    détention.<br />

                    III.-Une fois le permis accordé, il doit être satisfait en permanence aux conditions prévues aux b
                    et c du 1° du II.<br />

                    IV.-En cas de constatation du défaut de permis de détention, le maire ou, à défaut, le préfet met en
                    demeure le propriétaire ou le détenteur du chien de
                    procéder à la régularisation dans le délai d'un mois au plus. En l'absence de régularisation dans le
                    délai prescrit, le maire ou, à défaut,
                    le préfet peut ordonner que l'animal soit placé dans un lieu de dépôt adapté à l'accueil ou à la
                    garde de celui-ci et peut faire procéder sans délai et
                    sans nouvelle mise en demeure à son euthanasie.<br />

                    Les frais afférents aux opérations de capture, de transport, de garde et d'euthanasie de l'animal
                    sont intégralement et directement mis à la
                    charge de son propriétaire ou de son détenteur.<br />

                    V.-Le présent article, ainsi que le I de l'article L. 211-13-1, ne sont pas applicables aux
                    personnes qui détiennent un chien
                    mentionné à l'article L. 211-12 à titre temporaire et à la demande de son propriétaire ou de son
                    détenteur.
                </article>
                <article>
                    <span class="article-number">Article L211-15</span><br />
                    I.-L'acquisition, la cession à titre gratuit ou onéreux, hormis les cas prévus au troisième alinéa
                    de l'article L. 211-11
                    ou au deuxième alinéa de l'article 99-1 du code de procédure pénale, l'importation et l'introduction
                    sur le territoire métropolitain
                    et sur celui de l'ensemble des collectivités territoriales d'outre-mer
                    et de la Nouvelle-Calédonie des chiens de la première catégorie mentionnée à l'article L. 211-12
                    sont interdites.

                    II.-La stérilisation des chiens de la première catégorie est obligatoire.
                    Cette stérilisation donne lieu à un certificat vétérinaire.
                </article>
                <article>
                    <span class="article-number">Article L211-16</span><br />
                    I.-L'accès des chiens de la première catégorie aux transports en commun, aux lieux publics à
                    l'exception de la voie publique et aux locaux ouverts au public est interdit.
                    Leur stationnement dans les parties communes des immeubles collectifs est également interdit.<br />
                    II.-Sur la voie publique, dans les parties communes des immeubles collectifs,
                    les chiens de la première et de la deuxième catégorie doivent être muselés et tenus en laisse par
                    une personne majeure.
                    Il en est de même pour les chiens de la deuxième catégorie dans les lieux publics, les locaux
                    ouverts au public
                    et les transports en commun.<br />
                    III.-Un bailleur ou un copropriétaire peut saisir le maire en cas de dangerosité d'un chien résidant
                    dans un des logements dont il est propriétaire.
                    Le maire peut alors procéder, s'il le juge nécessaire, à l'application des mesures prévues à
                    l'article L. 211-11.<br />
                </article>
                <article>
                    <span class="article-number">Article L211-17</span><br />
                    Le dressage des chiens au mordant n'est autorisé que dans le cadre des activités de sélection canine
                    encadrées par
                    une association agréée par le ministre chargé de l'agriculture et des activités de surveillance,
                    de gardiennage et de transport de fonds.<br />
                    Seuls les dresseurs détenant un certificat de capacité peuvent exercer l'activité de dressage des
                    chiens au mordant
                    et acquérir des objets et des matériels destinés à ce dressage. Les prestations de services
                    effectuées en France, à titre temporaire
                    et occasionnel par les professionnels ressortissants d'un Etat membre de l'Union européenne, ou d'un
                    autre Etat partie à l'accord sur l'Espace
                    économique européen, établis sur le territoire d'un de ces Etats sont régies par l'article L. 204-1
                    et, le cas échéant, par l'article L. 204-2.
                    Il en est de même pour les responsables des activités de sélection canine mentionnées à l'alinéa
                    précédent. Le certificat de capacité est délivré
                    par l'autorité administrative aux candidats justifiant d'une aptitude professionnelle.<br />
                    L'acquisition, à titre gratuit ou onéreux, par des personnes non titulaires du certificat de
                    capacité, d'objets et de matériels destinés
                    au dressage au mordant est interdite. Le certificat de capacité doit être présenté au vendeur avant
                    toute cession.<br />
                    Celle-ci est alors inscrite sur un registre spécial tenu par le vendeur ou le cédant et mis à la
                    disposition des autorités de police et
                    des administrations chargées de l'application du présent article quand elles le demandent.
                </article>
                <article>
                    <span class="article-number">Article D211-3-1 </span><br />
                    Modifié par Décret n°2018-820 du 27 septembre 2018 - art. 1
                    L'évaluation comportementale prévue à l'article L. 211-14-1 est réalisée dans le cadre d'une
                    consultation vétérinaire.
                    Elle a pour objet d'apprécier le danger potentiel que peut représenter un chien. L'évaluation
                    comportementale est effectuée,
                    sur des chiens préalablement identifiés conformément aux dispositions de l'article L. 212-10, <br />
                    par un vétérinaire inscrit sur une liste départementale établie par le Conseil national de l'ordre
                    des vétérinaires.
                </article>
                <article>
                    <span class="article-number">Article D211-3-2</span></br>
                    Création Décret n°2008-1158 du 10 novembre 2008 - art. 1
                    Le vétérinaire en charge de l'évaluation comportementale classe le chien à l'un des quatre niveaux
                    de risque de dangerosité suivants : <br />
                    <span class="indent"></span>Niveau 1 : le chien ne présente pas de risque particulier de dangerosité
                    en dehors de ceux inhérents à l'espèce canine. </br>
                    <span class="indent"></span>Niveau 2 : le chien présente un risque de dangerosité faible pour
                    certaines personnes ou dans certaines situations.</br>
                    <span class="indent"></span>Niveau 3 : le chien présente un risque de dangerosité critique pour
                    certaines personnes ou dans certaines situations.</br>
                    <span class="indent"></span>Niveau 4 : le chien présente un risque de dangerosité élevé pour
                    certaines personnes ou dans certaines situations.</br>
                    Selon le niveau de classement du chien, le vétérinaire propose des mesures préventives visant à
                    diminuer la dangerosité du chien évalué
                    et émet des recommandations afin de limiter les contacts avec certaines personnes et les situations
                    pouvant générer des risques.</br>
                    Il peut conseiller de procéder à une nouvelle évaluation comportementale et indiquer le délai qui
                    doit s'écouler entre les deux évaluations.</br>
                    En cas de classement du chien au niveau de risque 4, le vétérinaire informe son détenteur ou son
                    propriétaire
                    qu'il lui est conseillé de placer l'animal dans un lieu de détention adapté ou de faire procéder à
                    son euthanasie.
                    Un lieu de détention adapté est un lieu dans lequel, sous la responsabilité du propriétaire ou du
                    détenteur, l'animal ne peut pas causer d'accident.
                    </br>
                    A l'issue de la visite, le vétérinaire en charge de l'évaluation communique les conclusions de
                    l'évaluation comportementale au maire
                    de la commune de résidence du propriétaire ou du détenteur du chien et, le cas échéant, au maire qui
                    a demandé
                    l'évaluation comportementale en application de l'article L. 211-11 ainsi qu'au fichier national
                    canin. Les modalités de transmission au fichier
                    national canin des informations relatives à
                    l'évaluation comportementale canine et la teneur de ces informations sont fixées par arrêté du
                    ministre de l'agriculture et de la pêche.
                </article>
                <article>
                    <span class="article-number">Article D211-3-2</span></br>
                    I.-Est puni de six mois d'emprisonnement et de 15 000 Euros d'amende le fait d'acquérir, de céder à
                    titre gratuit ou onéreux, hormis les cas prévus au troisième alinéa du I de l'article L. 211-11 ou
                    au troisième alinéa de l'article L. 211-29, d'importer ou d'introduire sur le territoire
                    métropolitain, en Guadeloupe, en Guyane, à la Martinique, à La Réunion, à Mayotte, à
                    Saint-Barthélemy, à Saint-Martin ou à Saint-Pierre-et-Miquelon des chiens de la première catégorie
                    mentionnée à l'article L. 211-12.<br />

                    Le fait de détenir un chien de la première catégorie sans avoir fait procéder à sa stérilisation est
                    puni des mêmes peines.<br />

                    II.-Les personnes physiques encourent également les peines complémentaires suivantes :<br />

                    <span class="indent"></span>1° La confiscation du ou des chiens concernés ;<br />

                    <span class="indent"></span>2° L'interdiction, pour une durée de cinq ans au plus, d'exercer une
                    activité professionnelle ou sociale dès lors que les facilités que procure cette activité ont été
                    sciemment utilisées pour préparer ou commettre l'infraction ;<br />

                    <span class="indent"></span>3° L'interdiction, pour une durée de cinq ans au plus, de détenir un
                    chien des première ou deuxième catégories mentionnées à l'article L. 211-12.<br />

                    III.-Les personnes morales reconnues pénalement responsables dans les conditions prévues à l'article
                    121-2 du code pénal des infractions prévues au I encourent les peines suivantes :<br />

                    <span class="indent"></span>1° L'amende, dans les conditions fixées à l'article 131-38 du même code
                    ;<br />

                    <span class="indent"></span>2° La confiscation du ou des chiens concernés ;<br />

                    <span class="indent"></span>3° L'interdiction, pour une durée de cinq ans au plus, de détenir un
                    chien des première ou deuxième catégories mentionnées à l'article L. 211-12 du présent code.<br />
                </article>
                <article>
                    <span class="article-number">Article D211-3-2</span></br>
                    Le fait, pour le propriétaire ou le détenteur d'un animal mis en demeure par l'autorité
                    administrative d'obtenir le permis de détention prévu à l'article L. 211-14, de ne pas procéder à la
                    régularisation requise dans le délai prescrit est puni de trois mois d'emprisonnement et de 3 750
                    euros d'amende.</br>

                    Les personnes physiques encourent également les peines complémentaires suivantes :</br>

                    <span class="indent"></span>1° La confiscation du ou des chiens concernés dans le cas où
                    l'euthanasie, telle que prévue à l'article L. 211-14, n'a pas été prononcée ;</br>

                    <span class="indent"></span>2° L'interdiction de détenir un animal à titre définitif ou non.</br>
                </article>
