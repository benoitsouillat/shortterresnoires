                                un droit à la reproduction LOF grâce à leur pedigree</li>
                                <li>La saillie soit déclarée</li>
                                <li>Le nombre de chiot soit déclaré, et que toute la portée soit inscrite auprès de la S.C.C, 
                                par l'éleveur possédant un numéro d'éleveur </li>
                            </ul>
                    </p>
                    <p class="col-9">
                        D’après l’article 276-5 du code Rural, Loi 99-5 du 6 Janvier 1999, 
                        l’inscription au LOF est obligatoire pour qu’un chien puisse légalement être qualifié de ‘Pure Race’.
